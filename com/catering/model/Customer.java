package com.catering.model;

public class Customer {
    private java.lang.Integer id;
    private java.lang.String nama;
    private java.lang.Integer noTelp;
    private java.lang.String alamat;
    private java.lang.String email;

    public Customer() {
    }

    public java.lang.Integer getId() {
        return id;
    }

    public void setId(java.lang.Integer id) {
        this.id = id;
    }

    public java.lang.String getNama() {
        return nama;
    }

    public void setNama(java.lang.String nama) {
        this.nama = nama;
    }

    public java.lang.Integer getNoTelp() {
        return noTelp;
    }

    public void setNoTelp(java.lang.Integer noTelp) {
        this.noTelp = noTelp;
    }

    public java.lang.String getAlamat() {
        return alamat;
    }

    public void setAlamat(java.lang.String alamat) {
        this.alamat = alamat;
    }

    public java.lang.String getEmail() {
        return email;
    }

    public void setEmail(java.lang.String email) {
        this.email = email;
    }

}
